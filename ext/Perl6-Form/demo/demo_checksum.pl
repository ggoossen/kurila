warn form '[{>>>>>>>>.<(20.10)<}]', 1.23456789;
warn form '[{>>>>>>>>.<<(20.10)}]', 1.23456789;

warn "\n";
warn      "[>>>>>>>>>>.<<<<<<<]\n";
warn form '[{>>>>>>>>>.(18.7)}]', 1.23456789;

