warn form '[{>>>>.<{10.3}<}]', 1.2345678;

warn "\n";
warn      "[>>>>>>>>>.<<<<<<<<<]\n";
warn form '[{>(20.10).<<<<<<<<<}]', 1.23456789;
warn form '[{(20.10)>.<<<<<<<<<}]', 1.23456789;
